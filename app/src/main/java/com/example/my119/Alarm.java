package com.example.my119;

public class Alarm {
    private String alarmName;
    private String whatNotice;

    public Alarm() { }

    public String getAlarmName() {
        return alarmName;
    }

    public void setAlarmName(String alarmName) {
        this.alarmName = alarmName;
    }

    public String getWhatNotice() {
        return whatNotice;
    }

    public void setWhatNotice(String whatNotice) {
        this.whatNotice = whatNotice;
    }
}
